      Sejak dikelola oleh Pemerintah Daerah, beberapa ruangan dibangun diantaranya ruang Laboratorium, 
      ruang Perawatan Kebidanan, ruang Radiologi, dan Ruang Operasi. Setelah 27 tahun yaitu pada tanggal 
      25 Februari 1991, Rumah Sakit Elim Rantepao dikembalikan kepada Gereja Toraja. Operasional rumah sakit 
      dilaksanakan berdasarkan SK Surat Ijin operasional RS Tipe C dengan Nomor 01/SK/IOP-RS/DP MPTSP/VI/2021 
