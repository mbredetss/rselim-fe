    <p class="text-justify">
      Rumah Sakit Elim Rantepao adalah rumah sakit tertua di Tana Toraja dan Toraja Utara yang beralamat 
      di Jl. Ahmad Yani no. 68 Rantepao, Toraja Utara-Sulawesi Selatan. Telah aktif melayani sejak berdirinya 
      pada tahun 1929 oleh pemerintah Belanda (Zelf Bestuur Luwu’), seluruh pelayanan dipercayakan kepada 
      Gereformeerde Zendingsbon. Pada tanggal 10 Oktober 1935, Asistent Resident Luwu’ atas nama De Gouverneur 
      menyerahkan hak milik Rumah Sakit Elim Rantepao kepada Gereformeerde Zendingsbon dengan membayar harga 
      sebesar 8.182.25 golden kepada pemerintah. Pada zaman pendudukan Jepang Rumah Sakit Elim Rantepao diambil 
      alih oleh Jepang. Setelah Perang Dunia II Jepang menyerah, lalu Rumah Sakit Elim Rantepao kembali kepada 
      pemiliknya Gereformeerde Zendingsbon (GZB). Kemudian Gereja Toraja berdiri pada Tahun 1947, dan GZB 
      menyerahkan semua asetnya kepada Gereja Toraja termasuk kepemilikan dan pengelolaan Rumah Sakit Elim Rantepao.
    </p>

